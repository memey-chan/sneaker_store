    public void initAppearance(){
        Object [] title = {"ID", "Name", "Price ($)", "Stock", "Size", "Gender"};
        DataAccessShoes das = new DataAccessShoes();
        ArrayList <Shoes> shoeList = das.showShoes();
        Object [][] ObjShoes = new Object[shoeList.size()][title.length];
        for (int i = 0; i < shoeList.size(); i++){
            Shoes shoes = shoeList.get(i);
            ObjShoes[i][0] = shoes.getId();
            ObjShoes[i][1] = shoes.getName();
            ObjShoes[i][2] = shoes.getPrice();
            ObjShoes[i][3] = shoes.getStock();
            ObjShoes[i][4] = shoes.getSize();
            ObjShoes[i][5] = shoes.getGender();
        }
        DefaultTableModel dtm = new DefaultTableModel(ObjShoes, title);
        mainTable.setModel(dtm);
        jScrollPane1.setViewportView(mainTable);
        mainTable.setEnabled(false);
    }

