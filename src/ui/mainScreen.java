    private void removeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeButtonActionPerformed
        if (nameTF.getText().isEmpty()||sizeTF.getText().isEmpty()||genderTF.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please fill all the required fields");
        }else{
            int input = JOptionPane.showConfirmDialog(null, "Are you sure?", "Confirm", JOptionPane.YES_NO_OPTION);
            System.out.println(input);
            if (input == 0){
                DataAccessShoes das = new DataAccessShoes();
                String name = nameTF.getText();
                int size = Integer.valueOf(sizeTF.getText());
                String gender = genderTF.getText();
                String condition = das.removeShoes(name, size, gender);
                if (condition.equals("true")){
                    JOptionPane.showMessageDialog(null, "Successfully removed");
                    nameTF.setText("");
                    priceTF.setText("");
                    stockTF.setText("");
                    sizeTF.setText("");
                    genderTF.setText("");
                }else{
                    JOptionPane.showMessageDialog(null, "Failed to remove");
                }
            }else{
                addOrRemoveScreen.setVisible(true);
            }
        }
    }//GEN-LAST:event_removeButtonActionPerformed

