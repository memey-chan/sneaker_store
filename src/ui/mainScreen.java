    private void searchTFKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchTFKeyReleased
        clearSearchButton.setVisible(true);
        String searchQuery = "%"+searchTF.getText()+"%";
        Object [] title = {"ID", "Name", "Price ($)", "Stock", "Size", "Gender"};
        DataAccessShoes das = new DataAccessShoes();
        ArrayList <Shoes> shoeList = das.showSpecific(searchQuery);
        Object [][] ObjShoes = new Object[shoeList.size()][title.length];
        for (int i = 0; i < shoeList.size(); i++){
            Shoes shoes = shoeList.get(i);
            ObjShoes[i][0] = shoes.getId();
            ObjShoes[i][1] = shoes.getName();
            ObjShoes[i][2] = shoes.getPrice();
            ObjShoes[i][3] = shoes.getStock();
            ObjShoes[i][4] = shoes.getSize();
            ObjShoes[i][5] = shoes.getGender();
        }
        DefaultTableModel dtm = new DefaultTableModel(ObjShoes, title);
        mainTable.setModel(dtm);
        jScrollPane1.setViewportView(mainTable);
        mainTable.setEnabled(false);
    }//GEN-LAST:event_searchTFKeyReleased

    private void minusStockMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_minusStockMouseClicked
        DataAccessShoes das = new DataAccessShoes();
        ArrayList <String> names = das.showNameDropdown();
        int nameIndex = nameDropdown.getSelectedIndex();
        String name = names.get(nameIndex);
        ArrayList <String> sizes = das.showSizeDropdown();
        int sizeIndex = sizeDropdown.getSelectedIndex();
        int size = Integer.valueOf(sizes.get(sizeIndex));
        int genderIndex = genderDropdown.getSelectedIndex();
        String gender;
        if (genderIndex == 0){
            gender = "men";
        }else{
            gender = "women";
        }
        int stock = Integer.valueOf(stockLabel.getText())-1;
        double price = Double.valueOf(priceLabel.getText());
        String confirm = das.changeShoes(price, stock, name, size, gender);
        if (confirm.equals("true")){
            stockLabel.setText(String.valueOf(stock));
        }else{
            increaseOrDecreaseScreen.setVisible(true);
        }
    }//GEN-LAST:event_minusStockMouseClicked

